    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        int sId = Integer.parseInt(student_id.getText());
        int retail_sub = Integer.parseInt(retail.getText());
        int english = Integer.parseInt(eng.getText());
        int com = Integer.parseInt(computer.getText());
        int type = Integer.parseInt(typingspeed.getText());
        String feed = jTextField1.getText();
        
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/maxeducationaltrust","root","A@ecis22al");
            Statement st = con.createStatement();
            
            st.executeUpdate("INSERT INTO mark(id,retail,english,computer,typing,feedback) VALUES('"+sId+"','"+retail_sub+"','"+english+"','"+com+"','"+type+"','"+feed+"')");
            JOptionPane.showMessageDialog(null,"Result Added Successfully");
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,e.toString());
        }
    }//GEN-LAST:event_jButton4ActionPerformed

